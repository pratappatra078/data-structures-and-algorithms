#include<iostream>
using namespace std;
int main()
{
    int num1 = 6;
    int num2 = 3;

    // is qual to (==)
    cout << (num1 == num2) << endl; // 0

    // not equal to(!=)
    cout << (num1!=num2) << endl; //1

    //greater than(>)
    cout <<(num1>num2) << endl; //1

    //less than (<)
    cout << (num1 < num2) << endl; //0

    // greater than or equal to (>=)
    cout << (num1 >= num2)<<endl; //1

    // less than or equal to (<=)
    cout <<(num1<=num2) << endl; // 0


}