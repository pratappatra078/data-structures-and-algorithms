#include <iostream>
using namespace std;
int main()
{
    int a;
    // equal to (=)
    a = 6;
    cout << a <<endl; //6

    // plus equal to(+=)
    a += 1;
    cout << a << endl; // 7

    // mainus equal to(-=)
    a -= 1;
    cout << a << endl; // 6

    // devide equal to(/=)
    a /= 2;
    cout << a << endl; // 3

    // modulud equal to (%=)
    a %= 3;
    cout << a << endl; // 0
}